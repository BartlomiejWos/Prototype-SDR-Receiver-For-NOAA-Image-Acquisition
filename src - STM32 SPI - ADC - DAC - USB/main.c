uint8_t MEAS_ADC_start(void) {
	// @brief	start ADC
	// @param	none
	// @return	U8 status, 0=OK (no error)

	uint8_t retval = 0;
	uint8_t s = 0;
	HAL_StatusTypeDef status = HAL_OK;
	HAL_ADCEx_Calibration_Start(&hadc1,ADC_DIFFERENTIAL_ENDED);
	status = HAL_ADCEx_Calibration_Start(&hadc1,ADC_DIFFERENTIAL_ENDED);		// calibrate ADC1
	if(status) retval |= (1 << s);
	s++;
	HAL_ADCEx_Calibration_Start(&hadc2,ADC_DIFFERENTIAL_ENDED);
	status = HAL_ADCEx_Calibration_Start(&hadc2,ADC_DIFFERENTIAL_ENDED);		// calibrate ADC2
	if(status) retval |= (1 << s);
	s++;
	HAL_Delay(10);

	// Start ADC1+ADC2 in Multimode configuration
	HAL_ADC_Start(&hadc2);
	HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t *)ADC1_Buffer, sizeof(ADC1_Buffer)); 	// start ADC1 /w Double Buffer
	//status = HAL_ADC_Start(&hadc2);																	// start ADC2 (slave) first!
	//status = HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t *)ADC1_Buffer, sizeof(ADC1_Buffer)); 	// start ADC1 /w Double Buffer

	if(status) retval |= (1 << s);
	s++;

	return(retval);
}

void MEAS_ADC1_eval(uint8_t pos) {

	uint16_t bP = 0;
	if(pos) bP = ADC1_BufferSize;

	memcpy(data_array + array_index, ADC1_Buffer + bP,sizeof(ADC1_Buffer)/2); //
	array_index +=4;


	if (array_index==MAX_DATA_ARRAY_SIZE) {


	    // Transmit the data to the PC
		memcpy(data_array_copy, data_array ,MAX_DATA_ARRAY_SIZE);
		CDC_Transmit_FS((uint8_t *)data_array_copy,MAX_DATA_ARRAY_SIZE);
		memset(data_array, 0, MAX_DATA_ARRAY_SIZE);
		array_index = 0;
	}

}


void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {

   if(hadc) MEAS_ADC1_eval(1);

}


void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc) {

   if(hadc) MEAS_ADC1_eval(0);

}


